    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $rules = [
            'year' => 'required',
            'salary' => 'required'
        ];
        $messages = [
            'year.required' => 'El campo ambiente es obligatorio.',
            'salary.required' => 'El campo ambiente es obligatorio.',
        ];

        $this->validate($request, $rules, $messages);

        $salary = new Salary();
        $salary->año = $request->year;
        $salary->valor = $request->salary;
        $salary->save();

        return redirect()->back()->with('success', 'Salario guardado con éxito.');
    }

